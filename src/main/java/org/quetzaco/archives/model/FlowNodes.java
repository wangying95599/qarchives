package org.quetzaco.archives.model;

import java.util.Date;

public class FlowNodes {
    private Long id;

    private String type;

    private Long flowId;

    private Long assigneeId;

    private Date createdDt;

    private Date deadLine;

    private Long assigneeBy;

    private Boolean needAssigneAction = false;

    private Boolean recordFlag = true;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type == null ? null : type.trim();
    }

    public Long getFlowId() {
        return flowId;
    }

    public void setFlowId(Long flowId) {
        this.flowId = flowId;
    }

    public Long getAssigneeId() {
        return assigneeId;
    }

    public void setAssigneeId(Long assigneeId) {
        this.assigneeId = assigneeId;
    }

    public Date getCreatedDt() {
        return createdDt;
    }

    public void setCreatedDt(Date createdDt) {
        this.createdDt = createdDt;
    }

    public Date getDeadLine() {
        return deadLine;
    }

    public void setDeadLine(Date deadLine) {
        this.deadLine = deadLine;
    }

    public Long getAssigneeBy() {
        return assigneeBy;
    }

    public void setAssigneeBy(Long assigneeBy) {
        this.assigneeBy = assigneeBy;
    }

    public Boolean getNeedAssigneAction() {
        return needAssigneAction;
    }

    public void setNeedAssigneAction(Boolean needAssigneAction) {
        this.needAssigneAction = needAssigneAction;
    }

    public Boolean getRecordFlag() {
        return recordFlag;
    }

    public void setRecordFlag(Boolean recordFlag) {
        this.recordFlag = recordFlag;
    }
}